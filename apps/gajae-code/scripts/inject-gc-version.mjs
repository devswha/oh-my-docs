#!/usr/bin/env node
// Fetches the current upstream gajae-code version and writes it to src/lib/version.ts.
// Strategy: try GitHub raw package.json over HTTPS; on failure, keep existing version.ts.
// Note: upstream Yeachan-Heo/gajae-code is a Bun monorepo whose root package.json has no
// top-level "version" field, so this fetch normally falls back to the committed seed.

import { writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const outPath = join(__dirname, '..', 'src', 'lib', 'version.ts');
const RAW_URL =
  'https://raw.githubusercontent.com/Yeachan-Heo/gajae-code/main/package.json';

async function fetchUpstreamVersion() {
  const res = await fetch(RAW_URL, { signal: AbortSignal.timeout(5000) });
  if (!res.ok) throw new Error(`HTTP ${res.status}`);
  const pkg = await res.json();
  if (!pkg.version || typeof pkg.version !== 'string') {
    throw new Error('package.json missing version field');
  }
  return pkg.version;
}

function writeVersionFile(version) {
  const content = `// Auto-generated by scripts/inject-gc-version.mjs. Do not edit by hand.\nexport const GC_VERSION = ${JSON.stringify(version)};\n`;
  writeFileSync(outPath, content, 'utf8');
}

try {
  const v = await fetchUpstreamVersion();
  writeVersionFile(v);
  console.log(`[inject-gc-version] GC_VERSION=${v}`);
} catch (err) {
  if (existsSync(outPath)) {
    console.warn(
      `[inject-gc-version] fetch failed (${err.message}); keeping existing version.ts`,
    );
  } else {
    // No upstream and no committed version: fail loudly rather than write a
    // stale literal that would silently downgrade the sidebar.
    console.error(
      `[inject-gc-version] fetch failed (${err.message}) and no existing ${outPath}.`,
    );
    process.exit(1);
  }
}
